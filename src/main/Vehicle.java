package main;

public class Vehicle {
	String Type;
	String Brand;
	String Name;
	String License;
	int Topspeed;
	int Gascap;
	
	public Vehicle(String type, String brand, String name, String license, int topspeed, int gascap) {
		this.Type = type;
		this.Brand = brand;
		this.Name = name;
		this.License = license;
		this.Topspeed = topspeed;
		this.Gascap = gascap;
	}

}

